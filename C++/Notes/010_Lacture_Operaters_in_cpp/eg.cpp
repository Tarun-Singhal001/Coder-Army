//  Examples of pre increment and post increment

#include <iostream>
#include<stdio.h>
using namespace std;
int main(){
// pre increment
    int a ,b;
    a = 10;
    b = ++a;
    cout<<a<<endl;
    cout<<b<<endl;

// post increment
    a = 10;
    b = a++;
    cout<<a<<endl;
    cout<<b<<endl;

    cout<< (5&7);
    

}